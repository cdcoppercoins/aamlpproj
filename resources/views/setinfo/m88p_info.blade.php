            <p>
        In 1988 Post again issued a dated 50-plate set, one representing each state. With some exception mentioned below, their
        design roughly matched that of the typical base for each of the states for that year.
    </p>
    <p>
        For the first time in two years collectors could, for a price, order a complete boxed and
        sealed set of the plates from the distribution center in Kankakee, Illinois. This, of course,
        added a bit of a common flavor to the set, which has limited their value going forward. Still
        today, these plates can be found for under $3 each by the set.
    </p>
    <p>The image to the right shows a few of the designs that are wide deviations from state issued plates (aside from the
    fact that the expiration stickers are generally not correct). Louisiana stopped issuing the black on white plate in 1984.
    Any "Sportsmen's Paradise" plates issued in 1988 would be blue on white. The 1988 issue from New Mexico had the state name
    in larger font above "Land of Enchantment." The type of issue used for the 1988 Post plate had not been state issued since 1982.
    Why the Virginia plate for Post had the state seal in the center is not known. No such plate had ever been issued by the state.</p>
